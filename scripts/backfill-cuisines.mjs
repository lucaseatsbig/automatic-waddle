#!/usr/bin/env node
// Backfill the `restaurant_cuisines` join table from the existing
// comma-separated values in `restaurants.cuisine`. Idempotent — re-running
// is safe because we INSERT OR IGNORE.
//
// Steps per restaurant:
//   1. Read its current cuisine string
//   2. Split on commas, trim, dedupe (case-insensitive), preserve original case
//   3. INSERT each into restaurant_cuisines with sort_order matching position
//   4. UPDATE restaurants.cuisine to just the first split value (so cards
//      display a single name; the join table keeps the full set)
//
// Usage:
//   node scripts/backfill-cuisines.mjs --dry-run   # preview SQL only
//   node scripts/backfill-cuisines.mjs             # write SQL file
//   node scripts/backfill-cuisines.mjs --apply     # write SQL file AND
//                                                    apply to local + remote
//
// On --apply we still write the SQL file first so you have a record of
// exactly what changed, in case you need to inspect it later.

import { writeFileSync } from 'node:fs';
import { execSync } from 'node:child_process';

const OUTPUT_SQL = 'scripts/backfill-cuisines.sql';

const args = process.argv.slice(2);
const flags = {
  dryRun: args.includes('--dry-run'),
  apply: args.includes('--apply'),
};

function escSql(v) {
  if (v == null) return 'NULL';
  return `'${String(v).replace(/'/g, "''")}'`;
}

function queryRemote(sql) {
  const flat = sql.replace(/"/g, '\\"').replace(/\s+/g, ' ').trim();
  const out = execSync(`npx wrangler d1 execute lucaseats-db --remote --json --command "${flat}"`, {
    encoding: 'utf8',
    stdio: ['pipe', 'pipe', 'inherit'],
    maxBuffer: 32 * 1024 * 1024,
  });
  return JSON.parse(out)[0].results;
}

console.log('Reading restaurants from remote...');

const rows = queryRemote(`
  SELECT id, name, cuisine
    FROM restaurants
   WHERE cuisine IS NOT NULL AND cuisine <> ''
   ORDER BY id
`);

console.log(`Found ${rows.length} restaurant(s) with a cuisine value.\n`);

const statements = [];
let totalSplits = 0;
let multiCuisineCount = 0;
const sampleMulti = [];

for (const r of rows) {
  const raw = String(r.cuisine ?? '').trim();
  if (!raw) continue;

  // Split on commas / slashes / "and" — pick whichever the data has.
  const parts = raw
    .split(/\s*[,/]\s*|\s+&\s+|\s+and\s+/i)
    .map((s) => s.trim())
    .filter((s) => s.length > 0);

  // Dedupe case-insensitively, keep first-seen casing.
  const seen = new Map();
  for (const p of parts) {
    const key = p.toLowerCase();
    if (!seen.has(key)) seen.set(key, p);
  }
  const cuisines = [...seen.values()];

  if (cuisines.length === 0) continue;

  if (cuisines.length > 1) {
    multiCuisineCount++;
    if (sampleMulti.length < 5) sampleMulti.push({ name: r.name, cuisines });
  }
  totalSplits += cuisines.length;

  // Insert each cuisine.
  for (let i = 0; i < cuisines.length; i++) {
    statements.push(
      `INSERT OR IGNORE INTO restaurant_cuisines (restaurant_id, cuisine, sort_order) VALUES (${r.id}, ${escSql(cuisines[i])}, ${i});`
    );
  }

  // If the original was multi-valued, normalise restaurants.cuisine to the
  // first one so cards display a single name. Single-cuisine rows are left
  // alone (their cuisine column already matches the first split value).
  if (cuisines.length > 1 && cuisines[0] !== raw) {
    statements.push(
      `UPDATE restaurants SET cuisine = ${escSql(cuisines[0])} WHERE id = ${r.id};`
    );
  }
}

console.log(`Plan:`);
console.log(`  ${rows.length} restaurant rows scanned`);
console.log(`  ${multiCuisineCount} restaurants had multi-valued cuisine`);
console.log(`  ${totalSplits} INSERT row(s) into restaurant_cuisines`);
console.log(`  ${statements.filter((s) => s.startsWith('UPDATE')).length} UPDATE(s) to restaurants.cuisine\n`);

if (sampleMulti.length > 0) {
  console.log(`Sample multi-cuisine restaurants:`);
  for (const s of sampleMulti) console.log(`  ${s.name}: [${s.cuisines.join(', ')}] → primary "${s.cuisines[0]}"`);
  console.log();
}

if (flags.dryRun) {
  console.log('--dry-run: not writing SQL file.');
  process.exit(0);
}

const header = `-- Generated by scripts/backfill-cuisines.mjs.
-- Idempotent — re-running is safe (INSERT OR IGNORE on the unique key).
-- Apply with:
--   wrangler d1 execute lucaseats-db --local --file=${OUTPUT_SQL}
--   wrangler d1 execute lucaseats-db --remote --file=${OUTPUT_SQL}

`;
writeFileSync(OUTPUT_SQL, header + statements.join('\n') + '\n');
console.log(`Wrote ${statements.length} statement(s) to ${OUTPUT_SQL}`);

if (flags.apply) {
  console.log('\nApplying to local...');
  execSync(`npx wrangler d1 execute lucaseats-db --local --file=${OUTPUT_SQL}`, {
    stdio: 'inherit',
  });
  console.log('\nApplying to remote...');
  execSync(`npx wrangler d1 execute lucaseats-db --remote --file=${OUTPUT_SQL}`, {
    stdio: 'inherit',
  });
  console.log('\nDone. Verify with:');
  console.log('  npx wrangler d1 execute lucaseats-db --remote --command "SELECT cuisine, COUNT(DISTINCT restaurant_id) AS n FROM restaurant_cuisines GROUP BY cuisine ORDER BY n DESC LIMIT 30"');
} else {
  console.log('\nNot applied. Run with --apply to write to local + remote, or apply manually:');
  console.log(`  npx wrangler d1 execute lucaseats-db --local --file=${OUTPUT_SQL}`);
  console.log(`  npx wrangler d1 execute lucaseats-db --remote --file=${OUTPUT_SQL}`);
}
